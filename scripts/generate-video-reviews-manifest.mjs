/**
 * Writes lib/video-reviews.manifest.ts before `next build` so the Kashmir
 * review is only bundled when the file is under Cloudflare’s 25 MiB limit
 * (or use NEXT_PUBLIC_REVIEW_VIDEO_KASHMIR_KERAN_URL for a remote URL).
 */
import fs from "fs";
import path from "path";

const MAX_BYTES = 25 * 1024 * 1024;
const KASHMIR_FILE = "Kashmir Keran Tour..mp4";
const filePath = path.join(process.cwd(), "public", "reviews-videos", KASHMIR_FILE);
const outPath = path.join(process.cwd(), "lib", "video-reviews.manifest.ts");

let kashmirLocalBundled = false;
try {
  const st = fs.statSync(filePath);
  kashmirLocalBundled = st.size > 0 && st.size < MAX_BYTES;
} catch {
  kashmirLocalBundled = false;
}

const banner = `/* Generated by scripts/generate-video-reviews-manifest.mjs — do not edit */\n`;

fs.writeFileSync(
  outPath,
  `${banner}export const kashmirLocalBundled = ${kashmirLocalBundled};\n`,
);

console.info(
  "[generate-video-reviews-manifest] kashmirLocalBundled = %s (%s)",
  kashmirLocalBundled,
  kashmirLocalBundled
    ? "local file is under 25 MiB"
    : "local file missing or ≥ 25 MiB — use remote URL or compress",
);
